class Sauropod
  def initialize(scientific_name, nickname, height, length, weight)
    @scientific_name = scientific_name
    @nickname = nickname
    @height = height
    @length = length
    @weight = weight
  end

  def nickname
    @nickname
  end

  def scientific_name
    @scientific_name
  end

  def height
    @height
  end

  def length
    @length
  end

  def weight
    @weight
  end






end
